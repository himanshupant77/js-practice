const month=3

switch (month) {
    case 1:
        console.log("Jan")
        break;                 //break very important to break control flow
    case 2:
        console.log("Feb")
        break;
    case 3:
        console.log("Mar")
        break;
    case 4:
        console.log("April")
        break;

    default:
        console.log("default month")
        break;
}